public class Switch
{
    public static void main(String[] args)
    {
        int s=3;
        System.out.println("This is switch case");

        switch (s)
        {
            case 1:
                System.out.println("monday");
                break;

            case 2:
                System.out.println("tuesday");
                break;

            default:
                System.out.println("enter valid value:");
        }

    }

}
