
           /* variable in java*/
public class Variable
{
    public static void main(String[] args)
    {
//all type of variable
        //integer type variable
        int a = 5;
        int b=3;
        int sum=a+b;
        System.out.println("integer variable: "+ sum);


        //floating variable
        float f = 5.99f;
        float g=3.4f;
        float floating=f+g;
        System.out.println("floating variable: "+floating);

        //character variable
        char Letter = 'D';
        System.out.println("character variable :" +Letter);


        //string
        String myText = "Hello";
        System.out.println("string variable:"+myText);

        //boolean variable
        boolean myBool = true;
        System.out.println("boolean variable:"+myBool);

    }
}
